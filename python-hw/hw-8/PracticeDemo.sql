	PLANT_PRODUCT_ID ,
	DATE_TRUNC('MONTH', DT)::DATE AS MNTH,
	avg(PLAN) AS AVG_PLAN ,
	avg(FACT) AS AVG_FACT
FROM OPERSVOD_STG.V_DDS_OPER
GROUP BY 
	PLANT_PRODUCT_ID ,
	DATE_TRUNC('MONTH', DT)::DATE
